const STARS_KEY = 'arrow-gh-stars'
const stars = reactive({ count: '' })

if (typeof window !== 'undefined') {
  const cached = sessionStorage.getItem(STARS_KEY)
  if (cached) {
    stars.count = cached
  } else {
    fetch('https://api.github.com/repos/standardagents/arrow-js')
      .then(r => r.json())
      .then(data => {
        if (data.stargazers_count) {
          const n = data.stargazers_count
          stars.count = n >= 1000 ? (n / 1000).toFixed(1).replace(/\.0$/, '') + 'k' : String(n)
          sessionStorage.setItem(STARS_KEY, stars.count)
        }
      })
      .catch(() => {})
  }
}

